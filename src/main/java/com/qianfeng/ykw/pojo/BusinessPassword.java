public class BusinessPassword {

  private int businessPasswordId;
  private String businessPassword;
  private int businessId;


  public int getBusinessPasswordId() {
    return businessPasswordId;
  }

  public void setBusinessPasswordId(int businessPasswordId) {
    this.businessPasswordId = businessPasswordId;
  }


  public String getBusinessPassword() {
    return businessPassword;
  }

  public void setBusinessPassword(String businessPassword) {
    this.businessPassword = businessPassword;
  }


  public int getBusinessId() {
    return businessId;
  }

  public void setBusinessId(int businessId) {
    this.businessId = businessId;
  }

}
